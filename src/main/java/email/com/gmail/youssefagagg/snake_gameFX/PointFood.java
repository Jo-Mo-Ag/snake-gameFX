package email.com.gmail.youssefagagg.snake_gameFX;

class PointFood {
    private final int x;
    private final int y;

    public PointFood(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
